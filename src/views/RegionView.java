    public void refresh() {
        model.setRowCount(0);
        Object[] row = new Object[3];
        List<Region> region = new ArrayList<>();
        region = irc.getAll();
        for (int i = 0; i < region.size(); i++) {
            row[0] = i + 1;
            row[2] = region.get(i).getId();
            row[3] = region.get(i).getName();
            model.addRow(row);
        }

    }

